<h4> Soalan Lazim </h4>
<ol>
  <li>Apakah E-Musolla?</li>
  <p>E-Musolla ialah sebuah sistem tempahan musolla secara atas talian. Fungsi sistem ini menyerupai sistem tempahan musolla secara manual, tetapi dalam sistem ini dipertingkatkan lagi fungsi dan sebagainya untuk mengatasi masalah yang dihadapi pengguna dalam sistem tempahan secara manual.
  </p>
  
  <li>Di manakah lokasi musolla?</li>
  <p>
    Terdapat lima musolla di unisza iaitu musolla blok Farabi, Khansa', Thoriq, Saodah dan Habibah. <a href="peta.pdf" target="_blank">Klik Peta Musolla</a>
  </p>
  
  <li>Apakah organisasi musolla?</li>
  <p>
    Setiap musolla diketuai oleh Yang di Pertua (YDP) dan Naib Yang di Pertua (NYDP) musolla. Terdapat juga setiausaha, bendahari dan ajk berkaitan. <a href="organisasi.pdf" target="_blank">Klik Organisasi Musolla</a>
  </p>
  
  <li>Siapakah yang boleh menggunakan sistem E-Musolla?</li>
  <p>Sistem ini boleh digunakan oleh tiga skop pengguna iaitu semua pelajar unisza, yang di pertua setiap musolla dan seorang staf unisza.
  </p>
  
  <li>Bagaimanakah saya boleh mengetahui aktiviti yang dijalankan di musolla?</li>
 <p>Setiap aktiviti yang dijalankan di setiap musolla akan di paparkan di Laman Utama E-Musolla</p>
  
  <li>Bagaimanakah prosedur untuk menempah musolla?</li>
 <p>Setiap pengguna yang ingin menggunakan sistem ini perlu log masuk ke dalam sistem terlebih dahulu. Kenmudian, pengguna perlu mengetahui status musolla yang boleh ditempah. Pengguna perlu mengisi borang tempahan selengkapnya dan tekan butang 'tempah'. <a href="manual.pdf" target="_blank">Klik Manual Tempahan</a>
  
  <li>Bagaimanakah saya boleh mengetahui status musolla yang boleh ditempah?</li>
  <p>Pelajar yang ingin menempah boleh melihat status keboleh adaan musolla di bahagian Kalendar Aktiviti Musolla</p> 
  
  <li>Berapa lama saya boleh mengetahui status dan respond tempahan saya?</li>
  <p>Pelajar yang menempah musolla perlu membuat tempahan seminggu awal dari tarikh program dan akan mendapat respond dalam masa tiga hari</p>
  
  <li>Adakah saya boleh mengakses sistem ini dimana sahaja?</li>
  <p>Ya, setiap pelajar boleh mengakses sistem ini dimana sahaja kerana E-Musolla ini merupakan sistem berasaskan web. </p>
  
  <li>Di mana saya boleh membuat aduan berkenaan kerosakan peralatan musolla?</li>
  <p>Peralatan eletronik atau kerosakan paip, perabot dan sebagainya yang menjadi hak milik unisza, pelajar boleh membuat aduan di portal aduan unisza. <a href="http://www.sentral.unisza.edu.my/modules.php?name=e-Aduan_Asrama" target="_blank">Klik Portal Aduan Unisza</a></p>
  
  
</ol>

